<div class="text-column">
	<h1>How to play at <b>100 Clicks</b></h1>

	<p>
		For one run, you have 100 clicks.<br>
		For one click, you should choose an action. Some actions give you some
		ressources, and some other will unlock new actions.<br>
		Actions may have a cost in ressource to be clickable, and some may
		need several clicks on them to be completed.<br>
	</p>
	<p>
		Following some actions will complete the story.
	</p>
	<p>
		When you have clicked 100 times. The run is over and you have to
		restart from the begining.<br>
		But all clicks done in previous run will continue to be applied on
		actions.<br>
		So if an action that needs 2 clicks. If click were done on run #1
		at click #10 and click #11 (which complete the action). So at run #2,
		if you do the same action at click #10 then the action will be
		completed immediately. And at run #3, this action will be completed
		at click #10 even if you choosed another action.<br>
		This mechanism helps you to progress faster and so to go further in
		the story.
	</p>
</div>
